package basics;

public class WhileDemo1 {
	public static void main(String[] args) {
		int i=1;
		while(i<=10) {
			System.out.println(i+" khalid");
			i=i+1;
		}
		System.out.println("**********Exiting loop******");
		System.out.println("**********Displaying loop from 10 to 1******");
		int j=10;
		while(j>0) {
			System.out.println(j);
			j=j-1;
		}
	}
}
