package basics;

public class ForDemo {
	public static void main(String[] args) {
		int i,j;
		System.out.println("******Display 1 to 25*********");
		for(i=1;i<=25;i++)
		{
			System.out.println(i);
		}
		System.out.println("*********Display from 55 to 20********");
		for(j=55;j>=20;j--)
		{
			System.out.println(j);
		}
	}	

}
